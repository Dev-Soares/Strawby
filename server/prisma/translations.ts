/**
 * Traduz um nome USDA/CNF estilo "Rice, brown, long-grain, cooked"
 * preservando estrutura e capitalizando o primeiro caractere.
 * Termos desconhecidos permanecem em inglês.
 */
export function translateName(original: string): string {
  if (!original) return original

  const tokens = original.split(/(\s+|,)/) // mantém separadores

  const translated = tokens.map((token) => {
    const trimmed = token.trim()
    if (!trimmed || trimmed === ',') return token

    const key = trimmed.toLowerCase().replace(/[.;:]$/, '')
    const trans = wordTranslations[key]
    if (trans !== undefined) {
      // Preserva sufixo de pontuação se houver
      const suffix = trimmed.match(/[.;:]$/)?.[0] ?? ''
      return token.replace(trimmed, trans + suffix)
    }
    return token
  })

  let result = translated.join('').replace(/\s+/g, ' ').replace(/\s+,/g, ',').trim()
  if (result.length > 0) {
    result = result[0].toUpperCase() + result.slice(1)
  }
  return result
}

/**
 * Decide se um nome traduzido tem qualidade boa o bastante.
 * Se >50% das palavras significativas ficaram em EN, retorna false.
 */
export function isTranslationGoodEnough(translated: string, original: string): boolean {
  const origWords = original.toLowerCase().split(/[\s,]+/).filter((w) => w.length > 3)
  if (origWords.length === 0) return true

  const stillEnglish = origWords.filter((w) => wordTranslations[w] === undefined).length
  return stillEnglish / origWords.length < 0.5
}
